// Массив вопросов с вариантами ответов и правильными ответами
const quiz = [
    {
        question: "Какой цвет небо?",
        options: ["1. Красный", "2. Синий", "3. Зелёный"],
        correctAnswer: 2 // номер правильного ответа (Синий)
    },
    {
        question: "Сколько дней в неделе?",
        options: ["1. Шесть", "2. Семь", "3. Восемь"],
        correctAnswer: 2 // номер правильного ответа (Семь)
    },
    {
        question: "Сколько у человека пальцев на одной руке?",
        options: ["1. Четыре", "2. Пять", "3. Шесть"],
        correctAnswer: 2 // номер правильного ответа (Пять)
    }
];

// Инициализация счётчика правильных ответов
let correctAnswers = 0;

// Цикл по всем вопросам
for (let i = 0; i < quiz.length; i++) {
    // Запрос ответа у пользователя
    const userAnswer = prompt(quiz[i].question);

    // Проверка ответа: сравниваем введённый номер с правильным
    if (userAnswer === quiz[i].correctAnswer.toString()) {
        correctAnswers++;
    }
}

// Вывод итогового результата
alert(`Вы набрали ${correctAnswers} правильных ответов из ${quiz.length}.`);
